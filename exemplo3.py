nota1 = float (input('Informe a primeira nota:')) # Estamos convertendo os valores textuais em valores decimais
nota2= float(input('Informe a segunda nota:'))
nota3 = float(input('Informe a terceira nota:')) 

media = (nota1 + nota2 + nota3)/3

print(f'Sua média é {media:.2f}') # .2f significa que teremos duas casas decimais após a vírgula    
